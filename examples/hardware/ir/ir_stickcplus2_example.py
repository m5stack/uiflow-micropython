import os, sys, io
import M5
from M5 import *
from hardware import *


ir = None


def setup():
    global ir

    M5.begin()
    ir = IR()


def loop():
    global ir
    M5.update()
    ir.tx(0x01, 0)


if __name__ == "__main__":
    try:
        setup()
        while True:
            loop()
    except (Exception, KeyboardInterrupt) as e:
        try:
            from utility import print_error_msg

            print_error_msg(e)
        except ImportError:
            print("please update to latest firmware")
